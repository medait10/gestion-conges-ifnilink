function translateNodeText(node, lang){
  if(lang === "fr") return;
  const dict = MB_EXTRA_TRANSLATIONS[lang] || {};
  let text = node.nodeValue;
  let trimmed = text.trim();
  if(!trimmed) return;
  if(dict[trimmed]){
    node.nodeValue = text.replace(trimmed, dict[trimmed]);
    return;
  }
  Object.keys(dict).sort((a,b)=>b.length-a.length).forEach(k=>{
    if(text.includes(k)) text = text.replaceAll(k, dict[k]);
  });
  node.nodeValue = text;
}

function translateAttributes(el, lang){
  if(lang === "fr") return;
  const dict = MB_EXTRA_TRANSLATIONS[lang] || {};
  ["placeholder","title","aria-label","value"].forEach(attr=>{
    const val = el.getAttribute && el.getAttribute(attr);
    if(val && dict[val]) el.setAttribute(attr, dict[val]);
  });
}

function applyAutoTranslations(lang){
  if(lang === "fr") return;
  document.documentElement.lang = lang;
  if(lang === "ar") document.documentElement.dir = "rtl";
  const walker = document.createTreeWalker(document.body, NodeFilter.SHOW_TEXT, {
    acceptNode(node){
      const p=node.parentElement;
      if(!p || ["SCRIPT","STYLE","TEXTAREA"].includes(p.tagName)) return NodeFilter.FILTER_REJECT;
      return node.nodeValue.trim().length>1 ? NodeFilter.FILTER_ACCEPT : NodeFilter.FILTER_REJECT;
    }
  });
  let n;
  while(n=walker.nextNode()) translateNodeText(n, lang);
  document.querySelectorAll("input,button,textarea,a,select").forEach(el=>translateAttributes(el, lang));
}

window.addEventListener("DOMContentLoaded",()=>applyAutoTranslations(window.MB_LANG||"fr"));
